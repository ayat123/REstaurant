package com.example.reeva.restaurant.model;

public class CuisinePojo {
	
	int id;
	String cusinename, cusinephoto, counter;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCusinename() {
		return cusinename;
	}
	public void setCusinename(String cusinename) {
		this.cusinename = cusinename;
	}
	public String getCusinephoto() {
		return cusinephoto;
	}
	public void setCusinephoto(String cusinephoto) {
		this.cusinephoto = cusinephoto;
	}
	public String getCounter() {
		return counter;
	}
	public void setCounter(String counter) {
		this.counter = counter;
	}
	
	

}
